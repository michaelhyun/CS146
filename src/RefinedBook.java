public class RefinedBook{
	private int index;
	private int ref;

	RefinedBook(){
		index = 0;
		ref = 0;
	}

	public void setIndex(int index){
		this.index = index;
	}

	public void setRef(int ref){
		this.ref = ref;
	}

	public int getIndex(){
		return index;
	}

	public int getRef(){
		return ref;
	}
}